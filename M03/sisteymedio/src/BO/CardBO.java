package BO;

public class CardBO {
	
	String card_id;
	String card_name;
	float card_value;
	int card_priority;
	float card_real_value;
	int deck_id;
	
	public String getCard_id() {
		return card_id;
	}
	public void setCard_id(String card_id) {
		this.card_id = card_id;
	}
	public String getCard_name() {
		return card_name;
	}
	public void setCard_name(String card_name) {
		this.card_name = card_name;
	}
	public float getCard_value() {
		return card_value;
	}
	public void setCard_value(float card_value) {
		this.card_value = card_value;
	}
	public int getCard_priority() {
		return card_priority;
	}
	public void setCard_priority(int card_priority) {
		this.card_priority = card_priority;
	}
	public float getCard_real_value() {
		return card_real_value;
	}
	public void setCard_real_value(float card_real_value) {
		this.card_real_value = card_real_value;
	}
	public int getDeck_id() {
		return deck_id;
	}
	public void setDeck_id(int deck_id) {
		this.deck_id = deck_id;
	}
	
	
	

}
